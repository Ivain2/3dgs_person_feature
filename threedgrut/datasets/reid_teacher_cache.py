"""
ReID Teacher Feature Cache Loader.

Offline cache for pre-extracted ReID teacher features.
Cache is generated by tools/build_reid_teacher_cache.py.

Cache key: (frame_id, camera_id, train_id, x1, y1, x2, y2)
  - Stable across annotation traversal order changes
  - Unique per person projection in a frame+camera

Cache Structure:
    {dataset_path}/reid_teacher_cache/
        {frame_id:08d}_{camera_id}_{train_id:04d}_{x1:04d}_{y1:04d}_{x2:04d}_{y2:04d}.pt

Each .pt file contains:
    {
        "frame_id": int,
        "camera_id": str,           # "C1" ~ "C7"
        "inst_idx": int,
        "train_id": int,            # 0-based continuous ID
        "raw_id": int,
        "bbox_xyxy": [xmin, ymin, xmax, ymax],
        "embedding": np.ndarray,    # [D] L2-normalized
        "embedding_dim": int,
        "model_name": str,
        "checkpoint": str,
    }
"""

from __future__ import annotations

import os
from typing import Dict, List, Optional, Tuple

from .cache_key import make_cache_key, parse_cache_filename


class ReidTeacherCache:
    """Loader for pre-extracted ReID teacher feature cache."""

    def __init__(self, dataset_path: str, cache_dir: str = None):
        self.dataset_path = dataset_path
        self.cache_dir = cache_dir or os.path.join(dataset_path, "reid_teacher_cache")
        self._cache_index: Dict[Tuple, str] = {}
        self._load_cache_index()

    def _load_cache_index(self) -> None:
        if not os.path.exists(self.cache_dir):
            print(f"[ReidTeacherCache] Cache directory not found: {self.cache_dir}")
            return

        for filename in os.listdir(self.cache_dir):
            if not filename.endswith(".pt"):
                continue

            try:
                cache_key = parse_cache_filename(filename)
            except (ValueError, IndexError):
                continue

            self._cache_index[cache_key] = os.path.join(self.cache_dir, filename)

        print(f"[ReidTeacherCache] Loaded {len(self._cache_index)} entries from {self.cache_dir}")

    def get(self, cache_key: tuple) -> Optional[Dict]:
        """Get cache entry by stable key.

        Args:
            cache_key: (frame_id, camera_id, train_id, x1, y1, x2, y2)

        Returns:
            dict with metadata + embedding, or None if not found
        """
        if cache_key not in self._cache_index:
            return None

        import torch

        file_path = self._cache_index[cache_key]
        try:
            data = torch.load(file_path, map_location="cpu", weights_only=False)
            return data
        except Exception as e:
            print(f"[ReidTeacherCache] Failed to load {file_path}: {e}")
            return None

    def verify(self, cache_key: tuple, annotation: dict, bbox_tol: int = 5) -> bool:
        """Verify cache entry matches annotation.

        Args:
            cache_key: stable key tuple
            annotation: dict with at least train_id, raw_id, bbox_xyxy
            bbox_tol: max pixel difference allowed per bbox coordinate

        Returns:
            True if consistent, False otherwise
        """
        cache_item = self.get(cache_key)
        if cache_item is None:
            return False

        if cache_item.get("train_id") != annotation.get("train_id"):
            return False

        cache_bbox = cache_item.get("bbox_xyxy", [])
        ann_bbox = annotation.get("bbox_xyxy", [])
        if cache_bbox and ann_bbox:
            for c, v in zip(cache_bbox, ann_bbox):
                if abs(c - v) > bbox_tol:
                    return False

        return True

    def __contains__(self, cache_key: tuple) -> bool:
        return cache_key in self._cache_index

    def __len__(self) -> int:
        return len(self._cache_index)
